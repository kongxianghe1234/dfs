    public static ArrayList<String> lst;

    public void adjacency_list()
    {
        lst = new ArrayList<String>();//(Integer)jSpinner1.getValue());
        list.add(jComboBox1);
        list.add(jComboBox2); 
        for (int i=0; i<(Integer)jSpinner1.getValue(); i++)
        {
            lst.add(new String());
        }
        for(int i = 0; i < lst.size(); i++)
        {
            for(int a=0; a < list.size(); a+=2)
            {
                if ((Integer)list.get(a).getSelectedIndex() == i)
                {
                    lst.set(i, lst.get(i) + " " + list.get(a+1).getSelectedIndex());
                }
            }
        }
    }
    
