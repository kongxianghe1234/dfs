    private ArrayList<JComboBox> list;
    
    public void setCmbBxs()
    {
        int n = (Integer)jSpinner1.getValue();
        String[] items = new String[n];
        for (int i=0; i<n; i++)
            items[i]=Integer.toString(i+1);
        JComboBox list1 = new JComboBox(items);
        JComboBox list2 = new JComboBox(items);
        list1.setBounds(jComboBox1.getX(), jComboBox1.getY()+step, jComboBox1.getWidth(), jComboBox1.getHeight());
        list2.setBounds(jComboBox2.getX(), jComboBox1.getY()+step, jComboBox2.getWidth(), jComboBox2.getHeight());
        list.add(list1);
        list.add(list2);
        jButton3.setBounds(jButton3.getX(), jComboBox1.getY()+step, jButton3.getWidth(), jButton3.getHeight());
        jPanel2.add(list.get(list.size()-2));
        jPanel2.add(list.get(list.size()-1));
        jPanel2.add(jButton3);
        createPanel(jComboBox1.getY()+30+step);
        step+=30;
    }
            
